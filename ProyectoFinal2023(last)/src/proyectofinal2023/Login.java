    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            conectarBD();
            String usuario = txtuser.getText();
            String password = txtpass.getText();
            sentenciaSQL = "SELECT nombre_usuario, password FROM usuarios WHERE estado LIKE 'Activo' and nombre_usuario=? and password=?";
            ps = con.prepareStatement(sentenciaSQL);
            ps.setString(1, usuario);
            ps.setString(2, password);
            rs = ps.executeQuery();
            
             if (rs.next()) {
                menuprincipal menu = new menuprincipal();
                menu.usuario = txtuser.getText();
                menu.setVisible(true);
                this.hide();
            } else {
                JOptionPane.showMessageDialog(null, "Nombre de usuario o contrase√±a incorrectos");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

